#include "libft.h"

static char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(const char *s)
{
	char	*copy;
	char	*src;

	src = (char *)s;
	copy = malloc(ft_strlen(src) + 1);
	if (copy != NULL)
		return (ft_strcpy(copy, src));
	return (copy);
}
